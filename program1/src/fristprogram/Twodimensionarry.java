package fristprogram;

public class Twodimensionarry {

	public static void main(String[] args) {
		
		int[][] twodim = new int[2][3];
		twodim[0][0]=24;
		twodim[0][1]=78;
		twodim[0][2]=98;
		twodim[1][0]=43;
		twodim[1][1]=18;
		twodim[1][2]=89;
		System.out.println(twodim[0][0]);
		System.out.println(twodim[0][1]);
		System.out.println(twodim[0][2]);
		System.out.println(twodim[1][0]);
		System.out.println(twodim[1][1]);
		System.out.println(twodim[1][2]);

	}

}
